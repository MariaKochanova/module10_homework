// Произвольный массив
let myArray = [1, 2, 3, 4, 5];

// Вывод количества элементов массива
console.log("Количество элементов массива: " + myArray.length);

// Перебор массива и вывод каждого элемента в консоль
console.log("Элементы массива:");
for (let i = 0; i < myArray.length; i++) {
  console.log(myArray[i]);
}
